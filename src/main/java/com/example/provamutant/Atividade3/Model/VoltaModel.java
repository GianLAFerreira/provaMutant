package com.example.provamutant.Atividade3.Model;


public class VoltaModel {
    private String tempo;
    private int numeroVolta;
    private String duracaoVolta;
    private String velocidadeMediaVolta;

    public String getTempo() {
        return tempo;
    }

    public void setTempo(String tempo) {
        this.tempo = tempo;
    }

    public int getNumeroVolta() {
        return numeroVolta;
    }

    public void setNumeroVolta(int numeroVolta) {
        this.numeroVolta = numeroVolta;
    }

    public String getDuracaoVolta() {
        return duracaoVolta;
    }

    public void setDuracaoVolta(String duracaoVolta) {
        this.duracaoVolta = duracaoVolta;
    }

    public String getVelocidadeMediaVolta() {
        return velocidadeMediaVolta;
    }

    public void setVelocidadeMediaVolta(String velocidadeMediaVolta) {
        this.velocidadeMediaVolta = velocidadeMediaVolta;
    }
}
